// variable is decalred
let num = 10

// Arithmetic operator only adds 15 to the variable but doesnt update the variable value
console.log(num + 15)
// 10 is logged
console.log(num)

// an assignment operator is used to add 15 and update the variables value
console.log(num += 15)
// 25 is now logged
console.log(num)


